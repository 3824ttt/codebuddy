def classify_by_rules(title="", subtitle="", keywords="", series_name="", description=""):
    """
    基于规则的图书分类
    返回: { category, clc_number, sub_category, confidence, method: 'rule' }
    """
    text = f"{title} {subtitle} {keywords} {series_name} {description}"

    best_match = None
    best_score = 0

    for cat_name, cat_info in DISCIPLINE_MAP.items():
        score = 0
        matched = 0
        for kw in cat_info.get("keywords", []):
            if kw in text:
                score += len(kw)
                matched += 1

        # 子类关键词匹配
        sub_match = None
        sub_score = 0
        sub_matched = 0
        for sub_name, sub_kws in cat_info.get("sub", {}).items():
            ss = 0
            sm = 0
            for kw in sub_kws:
                if kw in text:
                    ss += len(kw)
                    sm += 1
            if ss > sub_score:
                sub_score = ss
                sub_matched = sm
                sub_match = sub_name
        score += sub_score * 0.5

        # 加权：匹配到的关键词数量有额外奖励
        score += (matched + sub_matched * 0.5) * 3

        if score > best_score:
            best_score = score
            best_match = {
                "category": cat_name,
                "clc_number": cat_info.get("clc", ""),
                "sub_category": sub_match or "",
                "confidence": min(1.0, round(score / 15, 2)),
                "method": "rule",
            }

    if best_match and best_match["confidence"] >= 0.05:
        return best_match

    return {
        "category": "综合性图书",
        "clc_number": "Z",
        "sub_category": "",
        "confidence": 0.05,
        "method": "rule",
    }


