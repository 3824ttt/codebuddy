AI_CONFIG = {
    "enabled": False,
    "api_url": "",       # e.g. "https://api.openai.com/v1/chat/completions"
    "api_key": "",
    "model": "gpt-3.5-turbo",
}

AI_CATEGORIES = list(DISCIPLINE_MAP.keys())


def load_ai_config():
    """从环境变量或配置文件加载 AI 配置"""
    config_file = os.path.join(os.path.dirname(__file__), 'ai_config.json')
    if os.path.exists(config_file):
        try:
            with open(config_file, 'r', encoding='utf-8') as f:
                cfg = json.load(f)
                AI_CONFIG.update(cfg)
        except Exception:
            pass

    for key in ["AI_API_URL", "AI_API_KEY", "AI_MODEL"]:
        env_key = key
        if key == "AI_API_URL":
            cfg_key = "api_url"
        elif key == "AI_API_KEY":
            cfg_key = "api_key"
        else:
            cfg_key = "model"
        val = os.environ.get(env_key, "")
        if val:
            AI_CONFIG[cfg_key] = val

    AI_CONFIG["enabled"] = bool(AI_CONFIG.get("api_url") and AI_CONFIG.get("api_key"))


def classify_by_ai(title="", author="", description=""):
    """
    使用 AI 进行模糊分类
    需要配置 api_url 和 api_key
    """
    if not AI_CONFIG["enabled"]:
        return None

    try:
        import urllib.request

        prompt = f"""请对以下图书进行学科分类，从以下类别中选择最匹配的一项：

可选类别：{', '.join(AI_CATEGORIES)}

图书信息：
- 书名：{title}
- 作者：{author or '未知'}
- 简介：{description or '无'}

请用 JSON 格式回复，包含：
{{"category": "分类名称", "sub_category": "子类（可选）", "clc_number": "中图分类号", "confidence": 0.8, "reason": "分类依据"}}
只返回 JSON，不要其他内容。"""

        body = json.dumps({
            "model": AI_CONFIG["model"],
            "messages": [
                {"role": "system", "content": "你是一位专业的图书分类编目员，精通中图分类法和学科分类体系。"},
                {"role": "user", "content": prompt},
            ],
            "temperature": 0.3,
            "max_tokens": 300,
        }).encode()

        req = urllib.request.Request(
            AI_CONFIG["api_url"],
            data=body,
            headers={
                "Content-Type": "application/json",
                "Authorization": f"Bearer {AI_CONFIG['api_key']}",
            },
        )

        resp = urllib.request.urlopen(req, timeout=15)
        result = json.loads(resp.read())

        content = result["choices"][0]["message"]["content"]
        # 提取 JSON（可能被 markdown 包裹）
        json_match = re.search(r'\{[^{}]*"category"[^{}]*\}', content, re.DOTALL)
        if json_match:
            ai_result = json.loads(json_match.group())
            ai_result["method"] = "ai"
            ai_result["confidence"] = min(1.0, float(ai_result.get("confidence", 0.6)))
            return ai_result

    except Exception as e:
        pass

    return None


def classify_book(title="", subtitle="", author="", keywords="",
                  series_name="", description="", use_ai=False):
    """
    综合分类：优先规则，可选 AI 增强
    返回: { category, clc_number, sub_category, confidence, method }
    """
    # 1. 规则分类
    result = classify_by_rules(title, subtitle, keywords, series_name, description)

    # 2. 如果置信度低且启用 AI，尝试 AI 增强
    if result["confidence"] < 0.3 and use_ai:
        ai_result = classify_by_ai(title, author, description)
        if ai_result and ai_result.get("confidence", 0) > result["confidence"]:
            result = ai_result

    return result


